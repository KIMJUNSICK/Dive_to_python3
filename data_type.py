import fractions
import math


# Number
# basic arithmatic operation with python

print(11 / 2)  # 5.5

# round down
print(11 // 2)  # 5
print(-11 / 2)  # -6

# square
print(11 ** 2)  # 121

# the rest
print(11 % 2)  # 1

# Fracition
x = fractions.Fraction(1, 3)
y = fractions.Fraction(2, 6)

print(x)  # 1/3

# python automatically reduce a fraction.
print(y)  # 1/3

# Trigonometry
pi = math.pi
print(pi)
print(math.sin(pi / 2))
print(math.tan(pi / 4))
